function homeSearchListings(){
  const input=(document.getElementById('homeSearchInput')?.value || '').trim();
  if(!input){ window.location.href='landscapers.html'; return; }
  const normalized=input.toLowerCase();
  const townEntry=Object.entries(TOWN_SLUGS).find(([slug,name]) => normalized === slug || normalized === name.toLowerCase());
  if(townEntry){ window.location.href=`${townEntry[0]}-landscapers.html`; return; }
  window.location.href=`landscapers.html?search=${encodeURIComponent(input)}`;
}
function wireHomeSearch(){
  const input=document.getElementById('homeSearchInput');
  if(input){ input.addEventListener('keydown', e=>{ if(e.key==='Enter') homeSearchListings(); }); }
  const mobile=document.getElementById('mobileSearchInput');
  if(mobile){ mobile.addEventListener('keydown', e=>{ if(e.key==='Enter'){ const q=mobile.value.trim(); window.location.href=q?`landscapers.html?search=${encodeURIComponent(q)}`:'landscapers.html'; } }); }
}
function setYear(){ const el=document.getElementById('year'); if(el) el.textContent=new Date().getFullYear(); }
function getParam(name){ return new URLSearchParams(window.location.search).get(name); }
function ratingText(item){
  if(item.rating && item.reviews) return `★ ${item.rating} (${item.reviews}+ reviews)`;
  if(item.rating) return `★ ${item.rating}`;
  return 'Review details not yet available';
}
function townMatches(item, town){
  if(!town) return true;
  const hay = `${item.town} ${item.primaryTown} ${(item.serviceAreaTowns||[]).join(' ')}`.toLowerCase();
  return hay.includes(town.toLowerCase());
}
function populateFilters(){
  const townFilter=document.getElementById('townFilter');
  const serviceFilter=document.getElementById('serviceFilter');
  if(townFilter){
    const towns=[...new Set(landscapers.flatMap(x => [x.primaryTown, ...(x.serviceAreaTowns||[])].filter(Boolean)))].sort();
    townFilter.innerHTML='<option value="">All towns</option>'+towns.map(t=>`<option value="${t}">${t}</option>`).join('');
    const paramTown=getParam('town'); if(paramTown) townFilter.value=paramTown;
  }
  if(serviceFilter){
    const services=[...new Set(landscapers.flatMap(x=>x.services))].sort();
    serviceFilter.innerHTML='<option value="">All services</option>'+services.map(s=>`<option value="${s}">${s}</option>`).join('');
    const paramService=getParam('service'); if(paramService) serviceFilter.value=paramService;
  }
}
function card(item){
  const officialLink = item.website ? `<a class="button secondary" href="${item.website}" target="_blank" rel="noopener">Official Website</a>` : '';
  return `<article class="listing-card">
    <div class="badges"><span class="badge trust-badge">Local business</span><span class="badge">${item.primaryTown || item.town}</span><span class="badge">${item.services[0]}</span><span class="badge">Service area listed</span></div>
    <h2>${item.name}</h2>
    <p class="rating">${ratingText(item)}</p>
    <p>${item.description}</p>
    <div class="card-actions"><a class="button primary" href="listing.html?id=${item.id}">View Profile</a>${officialLink}</div>
  </article>`;
}
function renderListings(){
  const grid=document.getElementById('listingGrid'); if(!grid) return;
  const search=(document.getElementById('searchInput')?.value || getParam('search') || '').toLowerCase();
  const town=document.getElementById('townFilter')?.value || getParam('town') || '';
  const service=document.getElementById('serviceFilter')?.value || getParam('service') || '';
  const filtered=landscapers.filter(item=>{
    const hay=`${item.name} ${item.town} ${item.primaryTown} ${(item.serviceAreaTowns||[]).join(' ')} ${item.services.join(' ')} ${item.description}`.toLowerCase();
    return (!search || hay.includes(search)) && townMatches(item,town) && (!service || item.services.includes(service));
  }).sort((a,b)=>(b.score||0)-(a.score||0));
  const count=document.getElementById('resultCount'); if(count) count.textContent=filtered.length;
  grid.innerHTML=filtered.map(card).join('') || '<p>No matching listings found. Try a nearby town or service.</p>';
}
function renderFeatured(){
  const grid=document.getElementById('featuredGrid'); if(!grid) return;
  grid.innerHTML=landscapers.slice(0,6).map(card).join('');
}
function renderTownPage(){
  const box=document.getElementById('townListings'); if(!box) return;
  const slug=document.body.dataset.townSlug; const town=TOWN_SLUGS[slug];
  const matches=landscapers.filter(x=>townMatches(x,town)).sort((a,b)=>(b.score||0)-(a.score||0)).slice(0,8);
  const count=document.getElementById('townCount'); if(count) count.textContent=matches.length;
  box.innerHTML=matches.map(card).join('') || `<p>Browse the full directory for landscapers serving ${town} and nearby towns.</p>`;
}
function renderProfile(){
  const profile=document.getElementById('profileContent'); if(!profile) return;
  const id=getParam('id'); const item=landscapers.find(entry=>entry.id===id) || landscapers[0];
  document.title=`${item.name} | ${item.primaryTown || item.town}, MA`;
  profile.innerHTML=`<p class="eyebrow">${item.primaryTown || item.town}, MA</p>
    <h1>${item.name}</h1><p class="profile-meta">${ratingText(item)} · Serving ${(item.serviceAreaTowns||[item.primaryTown]).join(', ')}</p>
    <h2>About ${item.name}</h2><p>${item.description}</p><h2>Services Offered</h2><ul>${item.services.map(service=>`<li>${service}</li>`).join('')}</ul>
    <h2>Reviews at a Glance</h2><p class="rating">${ratingText(item)}</p>
    <h2>Contact</h2><p><a class="button primary" href="${item.website}" target="_blank" rel="noopener">Visit Official Website</a></p>
    <p><em>Before contacting a company, confirm current services, availability, insurance, and quote details directly with the business.</em></p>`;
}
